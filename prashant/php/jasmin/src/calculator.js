function Calculator()	{

}
	
	Calculator.prototype.add = function($num1, $num2) {
		return $num1 + $num2;
	}
	Calculator.prototype.sub = function($num1, $num2) {
		return $num1 - $num2;
	}
	Calculator.prototype.mul = function($num1, $num2) {
		return $num1 * $num2;
	}
	Calculator.prototype.div = function($num1, $num2) {
		return $num1 / $num2;
	}

